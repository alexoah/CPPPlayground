/**
 *	from C++ Loops: Exercise 1 ( https://www.w3schools.com/cpp/exercise.asp?filename=exercise_while_loop1 )
 *
 *	question:
 *	Print i as long as i is less than 6.
 *
 *	int i = 1;
 *	_____ (i < 6) {
 *		cout << i << "\n";
 *		___;
 *	}
 *
 */
int i = 1;
while (i < 6) {
	cout << i << "\n";
	i++;
}